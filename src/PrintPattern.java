public class PrintPattern {
    public static void main(String[] args)
    {
        int i;
        int j;
        int num = 4;
        for(i= 1; i<=num; i++)
        {
            for(j=1; j<=num; j++)
            {
                System.out.println("*");

            }
            System.out.println("\n");
        }
    }
}
